_DEFAULT_RUBRIC = "Прочее"


def _load_overrides() -> dict[str, dict]:
    path = Path(__file__).with_name("rubric_overrides.json")
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError:
        return {}


# Web-verified {code: {"rubric", "name", "conf"}} for bad (shade-only) names.
RUBRIC_OVERRIDES: dict[str, dict] = _load_overrides()


def classify_rubric_by_name(name: str) -> str:
    """Rubric from the product name via keyword rules + shade fallback."""
    n = (name or "").lower()
    for rubric, keywords in _RULES:
        for kw in keywords:
            if kw in n:
                return rubric
    tokens = re.findall(r"[а-яёa-z]+", n)
    if tokens and len(tokens) <= 5 and any(t in _COLOR_WORDS for t in tokens):
        return "Макияж"
    return _DEFAULT_RUBRIC


def resolve_rubric(code: str, name: str) -> str:
    """Final rubric for a catalog code: web override wins, else by-name."""
    ov = RUBRIC_OVERRIDES.get(str(code))
    if ov and ov.get("rubric") in RUBRICS:
        return ov["rubric"]
    return classify_rubric_by_name(name)


def resolve_name(code: str, name: str) -> str:
    """Corrected display name: the web-verified full name if we have a better
    one for this code (bad shade-only name), else the original name."""
    ov = RUBRIC_OVERRIDES.get(str(code))
    if ov:
        better = (ov.get("name") or "").strip()
        if better:
            return better
    return name
